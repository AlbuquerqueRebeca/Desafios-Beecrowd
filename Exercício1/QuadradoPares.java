import java.util.Scanner;

public class QuadradoPares { 

public static void main(String[] args) {
    
 
		Scanner entrada = new Scanner(System.in);
		
		int a = entrada.nextInt();
		
		
		
		
		
		for(int i = 2; i <= a; i += 2) {
			int par = i * i;
			
			System.out.printf("%d^2 = %d\n", i, + par);
		}
		
		
		
































































}


























}