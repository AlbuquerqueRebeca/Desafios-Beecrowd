package CalculoJogo;

import java.util.Scanner;

public class TempoJogo {

public static void main(String[] args) {
    
    Scanner entrada = new Scanner(System.in);
	
	int horaInicial = entrada.nextInt();
	int minutoInicial = entrada.nextInt();
	int horaFinal = entrada.nextInt();
	int minutoFinal = entrada.nextInt();
	
	
	int duraçao1; 
	if(horaInicial < horaFinal)	{
		duraçao1 = horaFinal - horaInicial;
	}else if(horaInicial == horaFinal) {
		if(minutoFinal > minutoInicial) {
	duraçao1 = 0;
	
		}else

			duraçao1 = 24;
	}else {
		duraçao1 = 24 - horaInicial + horaFinal;
	}
	
	
	int duraçao2;
	if(minutoInicial < minutoFinal) {
		duraçao2 = minutoFinal - minutoInicial;
	
	}else if(minutoInicial > minutoFinal) {
		duraçao2 = 60 - minutoInicial + minutoFinal;
		duraçao1 --;
	}else {
		duraçao2 = 0;
	}
	
	
	System.out.println("O JOGO DUROU " + duraçao1 + " HORA(S) E " + duraçao2 + " MINUTO(S)");
		
		













































































































}




























































































































































































}